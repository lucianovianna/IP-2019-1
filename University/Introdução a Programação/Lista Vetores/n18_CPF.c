#include <stdio.h>

int main(){
	int n, i, j, v[11], b1, b2;
	scanf("%d", &n);
	
	for(i=0; i<n; i++){
		for(j=1; j<=11; j++){
			scanf("%d", &v[j]);
		}
///////////////////////////////////////////////
		b1 = ( (v[1])+(v[2]*2)+(v[3]*3)+(v[4]*4)+(v[5]*5)+(v[6]*6)+(v[7]*7)+(v[8]*8)+(v[9]*9) ) % 11;
		if(b1 == 10) b1 = 0;
///////////////////////////////////////////////
		b2 = ( (v[1]*9)+(v[2]*8)+(v[3]*7)+(v[4]*6)+(v[5]*5)+(v[6]*4)+(v[7]*3)+(v[8]*2)+(v[9]) ) % 11;
		if(b2 == 10) b2 = 0;
////////////////////////////////////////////////	
		if(b1 == v[10] && b2 == v[11])
			printf("CPF valido\n");
		else
			printf("CPF invalido\n");
	}
	
	return 0;
}






















